package com.retarus.fax.base.common;

/**
 * @author thiagon
 * <p>
 * Enum for the HTTP methods used in the REST API.
 * <p>
 * Possible values are:<p>
 * POST: HTTP POST<p>
 * PUT: HTTP PUT<p>
 * GET: HTTP GET<p>
 * DELETE: HTTP DELETE<p>
 * HEAD: HTTP HEAD<p>
 * OPTIONS: HTTP OPTIONS
 */
public enum HttpMethod {
    POST,
    PUT,
    GET,
    DELETE,
    HEAD,
    OPTIONS
}
